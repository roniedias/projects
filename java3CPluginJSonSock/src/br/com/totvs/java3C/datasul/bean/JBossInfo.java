package br.com.totvs.java3C.datasul.bean;

public class JBossInfo {
	
	private String protocolo;
	private String porta;
	private String nomeHost;
	private String ipHost;
	private String seqAppSer;
	private String dirJBossCliente;
	
	public String getProtocolo() {
		return protocolo;
	}
	public void setProtocolo(String protocolo) {
		this.protocolo = protocolo;
	}
	
	public String getPorta() {
		return porta;
	}
	public void setPorta(String porta) {
		this.porta = porta;
	}
	
	public String getNomeHost() {
		return nomeHost;
	}
	public void setNomeHost(String nomeHost) {
		this.nomeHost = nomeHost;
	}
	
	public String getIpHost() {
		return ipHost;
	}
	public void setIpHost(String ipHost) {
		this.ipHost = ipHost;
	}
	
	public String getSeqAppSer() {
		return seqAppSer;
	}
	public void setSeqAppSer(String seqAppSer) {
		this.seqAppSer = seqAppSer;
	}
	
	public String getDirJBossCliente() {
		return dirJBossCliente;
	}
	public void setDirJBossCliente(String dirJBossCliente) {
		this.dirJBossCliente = dirJBossCliente;
	}
	
	
}
