package br.com.totvs.java3C.fluig.bean;

public class TiposServicosFluig {

	private String codTipSrv;
	private String nomeTipSrv;
	private String ip;
	private String porta;
	private String monitora;
	
	
	public String getCodTipSrv() {
		return codTipSrv;
	}
	
	public void setCodTipSrv(String codTipSrv) {
		this.codTipSrv = codTipSrv;
	}
	
	
	public String getNomeTipSrv() {
		return nomeTipSrv;
	}
	
	public void setNomeTipSrv(String nomeTipSrv) {
		this.nomeTipSrv = nomeTipSrv;
	}
	
	
	public String getIp() {
		return ip;
	}
	
	public void setIp(String ip) {
		this.ip = ip;
	}
	
	
	public String getPorta() {
		return porta;
	}
	
	public void setPorta(String porta) {
		this.porta = porta;
	}
	
	
	public String getMonitora() {
		return monitora;
	}
	
	public void setMonitora(String monitora) {
		this.monitora = monitora;
	}
	
	
}
