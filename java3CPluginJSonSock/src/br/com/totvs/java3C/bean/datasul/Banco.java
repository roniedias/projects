package br.com.totvs.java3C.bean.datasul;

public class Banco {
	
	private String nomeFisico;
	private String alias;
	private String ip;
	private String porta;
	private String usuario;
	private String senha;
	private String urlConexao;

	public String getNomeFisico() {
		return nomeFisico;
	}
	public void setNomeFisico(String nomeFisico) {
		this.nomeFisico = nomeFisico;
	}
	
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
	public String getPorta() {
		return porta;
	}
	public void setPorta(String porta) {
		this.porta = porta;
	}
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	public String getUrlConexao() {
		return urlConexao;
	}
	public void setUrlConexao(String urlConexao) {
		this.urlConexao = urlConexao;
	}
	
	
	
}
