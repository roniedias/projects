package br.com.totvs.java3C.bean.datasul;

public class AppServer {
	private String ip;
	private String porta;
	private String instanciaApp;
	private String nomeHost;
	
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
	public String getPorta() {
		return porta;
	}
	public void setPorta(String porta) {
		this.porta = porta;
	}
	
	public String getInstanciaApp() {
		return instanciaApp;
	}
	public void setInstanciaApp(String instanciaApp) {
		this.instanciaApp = instanciaApp;
	}
	
	public String getNomeHost() {
		return nomeHost;
	}
	public void setNomeHost(String nomeHost) {
		this.nomeHost = nomeHost;
	}

	

}
