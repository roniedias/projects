package br.com.debugger3c.model;

public class ClientEnvironmentInfo {
	
	public String zbaCodAmb;
	public String zbeTipAmb;
	public String zb1Descr;
	public String zbbProdut;
	public String zbdParam;
	public String zc1Descr;
	
	
	public ClientEnvironmentInfo() {}
	

	public ClientEnvironmentInfo(String zbaCodAmb, String zbeTipAmb, String zb1Descr, String zbbProdut, String zbdParam, String zc1Descr) {
		this.zbaCodAmb = zbaCodAmb;
		this.zbeTipAmb = zbeTipAmb;
		this.zb1Descr = zb1Descr;
		this.zbbProdut = zbbProdut;
		this.zbdParam = zbdParam;
		this.zc1Descr = zc1Descr;
	}
	
	
	public String getZbaCodAmb() {
		return zbaCodAmb;
	}
	public void setZbaCodAmb(String zbaCodAmb) {
		this.zbaCodAmb = zbaCodAmb;
	}
	
	
	
	
	public String getZbeTipAmb() {
		return zbeTipAmb;
	}
	public void setZbeTipAmb(String zbeTipAmb) {
		this.zbeTipAmb = zbeTipAmb;
	}
	
	
	public String getZb1Descr() {
		return zb1Descr;
	}
	public void setZb1Descr(String zb1Descr) {
		this.zb1Descr = zb1Descr;
	}
	
	
	public String getZbbProdut() {
		return zbbProdut;
	}
	public void setZbbProdut(String zbbProdut) {
		this.zbbProdut = zbbProdut;
	}
	
	
	
	public String getZbdParam() {
		return zbdParam;
	}
	public void setZbdParam(String zbdParam) {
		this.zbdParam = zbdParam;
	}
	
	
	public String getZc1Descr() {
		return zc1Descr;
	}
	public void setZc1Descr(String zc1Descr) {
		this.zc1Descr = zc1Descr;
	}
	
	
	

}
